const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divid: function (a, b) {
    console.log(a / b);
  },
  multiple: function (a, b) {
    console.log(a * b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(2, 2);
calculator.minus(2, 2);
calculator.divid(2, 2);
calculator.multiple(2, 2);
calculator.power(2, 3);
